package mx.com.grupogigante.gestionvivienda.domain.dto.catalogos;

/**
 * Clase que encapsula los fields de Regiones
 * Fecha de creación: 19/07/2012               
 */

public class RegionesDto {

	private String pais;
	private String region;
	private String regionx;
	
	/**
	 * @return the pais
	 */
	public String getPais() {
		return pais;
	}
	/**
	 * @param pais the pais to set
	 */
	public void setPais(String pais) {
		this.pais = pais;
	}
	/**
	 * @return the region
	 */
	public String getRegion() {
		return region;
	}
	/**
	 * @param region the region to set
	 */
	public void setRegion(String region) {
		this.region = region;
	}
	/**
	 * @return the regionx
	 */
	public String getRegionx() {
		return regionx;
	}
	/**
	 * @param regionx the regionx to set
	 */
	public void setRegionx(String regionx) {
		this.regionx = regionx;
	}

}
